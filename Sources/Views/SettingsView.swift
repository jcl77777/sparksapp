                            HStack {
                                Text("üí°")
                                    .font(.system(size: 20))

                                Text(NSLocalizedString("notification_unorganized_hint", comment: "ÊèêÈÜíÊÇ®Êï¥ÁêÜÊú™ÂàÜÈ°ûÁöÑÈùàÊÑüÔºå‰øùÊåÅÂâµÊÑèÊµÅÊö¢"))
