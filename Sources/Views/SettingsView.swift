struct TagManagerView: View {
    @EnvironmentObject var viewModel: InspirationViewModel
    @Environment(\.presentationMode) var presentationMode
    @State private var newTagName: String = ""
    @State private var editingTag: Tag?
    @State private var editingTagName: String = ""
    @State private var showEditSheet: Bool = false
    @State private var showDeleteAlert: Bool = false
    @State private var tagToDelete: Tag?
    @FocusState private var isTextFieldFocused: Bool

    var body: some View {
        VStack(spacing: 0) {
            // Gradient Header
            GradientHeader(
                title: "üè∑Ô∏è " + NSLocalizedString("tag_manager_section_title", comment: "Ê®ôÁ±§ÁÆ°ÁêÜ"),
                gradientColors: AppDesign.Colors.blueGradient
            )

            ScrollView {
                VStack(spacing: AppDesign.Spacing.standard) {
                    // Êñ∞Â¢ûÊ®ôÁ±§ÂçÄÂüü
                    VStack(alignment: .leading, spacing: AppDesign.Spacing.small) {
                        Text(NSLocalizedString("tag_manager_new_tag_placeholder", comment: "Êñ∞Â¢ûÊ®ôÁ±§ÂêçÁ®±"))
                            .font(.system(size: AppDesign.Typography.bodySize, weight: .bold, design: .monospaced))
                            .foregroundColor(AppDesign.Colors.textPrimary)

                        HStack(spacing: AppDesign.Spacing.small) {
                            PixelTextField(
                                text: $newTagName,
                                placeholder: NSLocalizedString("tag_manager_new_tag_placeholder", comment: "Êñ∞Â¢ûÊ®ôÁ±§ÂêçÁ®±"),
                                icon: "üè∑Ô∏è"
                            )
                            .focused($isTextFieldFocused)

                            PixelButton(
                                "‚ûï",
                                color: AppDesign.Colors.green
                            ) {
                                addTag()
                            }
                            .disabled(newTagName.trimmingCharacters(in: .whitespaces).isEmpty)
                            .opacity(newTagName.trimmingCharacters(in: .whitespaces).isEmpty ? 0.5 : 1.0)
                            .frame(width: 60)
                        }
                    }

                    // Ê®ôÁ±§ÂàóË°®
                    VStack(alignment: .leading, spacing: AppDesign.Spacing.small) {
                        Text(NSLocalizedString("tag_manager_section_title", comment: "Ê®ôÁ±§ÁÆ°ÁêÜ"))
                            .font(.system(size: AppDesign.Typography.bodySize, weight: .bold, design: .monospaced))
                            .foregroundColor(AppDesign.Colors.textPrimary)

                        if viewModel.availableTags.isEmpty {
                            PixelCard(borderColor: AppDesign.Colors.gray) {
                                Text(NSLocalizedString("tag_manager_no_tag", comment: "ÁõÆÂâçÊ≤íÊúâÊ®ôÁ±§"))
                                    .font(.system(size: AppDesign.Typography.bodySize, design: .monospaced))
                                    .foregroundColor(AppDesign.Colors.textSecondary)
                                    .padding(AppDesign.Spacing.standard)
                            }
                        } else {
                            VStack(spacing: AppDesign.Spacing.small) {
                                ForEach(viewModel.availableTags, id: \.objectID) { tag in
                                    PixelCard(borderColor: AppDesign.Colors.purple) {
                                        HStack {
                                            Text("üè∑Ô∏è")
                                                .font(.system(size: 20))

                                            Text(tag.name ?? "")
                                                .font(.system(size: AppDesign.Typography.bodySize, weight: .bold, design: .monospaced))
                                                .foregroundColor(AppDesign.Colors.textPrimary)

                                            Spacer()

                                            Button(action: {
                                                editingTag = tag
                                                editingTagName = tag.name ?? ""
                                                showEditSheet = true
                                            }) {
                                                Text("‚úèÔ∏è")
                                                    .font(.system(size: 18))
                                            }

                                            Button(action: {
                                                tagToDelete = tag
                                                showDeleteAlert = true
                                            }) {
                                                Text("üóëÔ∏è")
                                                    .font(.system(size: 18))
                                            }
                                        }
                                        .padding(AppDesign.Spacing.standard)
                                    }
                                }
                            }
                        }
                    }

                    // ÈóúÈñâÊåâÈàï
                    PixelButton(
                        NSLocalizedString("common_close", comment: "ÈóúÈñâ"),
                        style: .secondary,
                        color: AppDesign.Colors.gray
                    ) {
                        presentationMode.wrappedValue.dismiss()
                    }
                }
                .padding(AppDesign.Spacing.standard)
            }
            .background(Color(.systemGroupedBackground))
        }
        .onAppear {
            DispatchQueue.main.asyncAfter(deadline: .now() + 0.3) {
                isTextFieldFocused = true
            }
        }
        .sheet(isPresented: $showEditSheet) {
            if let tag = editingTag {
                EditTagView(tag: tag, tagName: $editingTagName) {
                    updateTag(tag: tag, newName: editingTagName)
                    showEditSheet = false
                } onCancel: {
                    showEditSheet = false
                }
                .environmentObject(viewModel)
            }
        }
        .alert(isPresented: $showDeleteAlert) {
            Alert(
                title: Text(NSLocalizedString("tag_manager_delete_confirm_title", comment: "Á¢∫ÂÆöË¶ÅÂà™Èô§ÈÄôÂÄãÊ®ôÁ±§ÂóéÔºü")),
                message: Text(tagToDelete?.name ?? ""),
                primaryButton: .destructive(Text(NSLocalizedString("common_delete", comment: "Âà™Èô§"))) {
                    if let tag = tagToDelete {
                        deleteTag(tag: tag)
                    }
                },
                secondaryButton: .cancel()
            )
        }
    }
    private func addTag() {
        viewModel.addTag(name: newTagName)
        newTagName = ""
    }
    private func updateTag(tag: Tag, newName: String) {
        viewModel.updateTag(tag: tag, newName: newName)
    }
    private func deleteTag(tag: Tag) {
        viewModel.deleteTag(tag: tag)
    }
}

